package task2;

// Класс точка
public class Dot {
    private final float x; // Координата X
    private final float y; // Координата Y

    // Конструктор
    public Dot(float x, float y) {
        this.x = x;
        this.y = y;
    }

    // Геттер для получения X
    public float getX() {
        return x;
    }

    // Геттер для получения Y
    public float getY() {
        return y;
    }

}
